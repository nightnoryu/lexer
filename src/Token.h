#pragma once

#include <string>

enum class TokenType
{
	BLANK,
	COMMENT,
	COMMA,
	SEMICOLON,
	COLON,
	DOT,

	ASSIGNMENT,
	IDENTIFIER,
	STRING_LITERAL,

	INT,
	DOUBLE,
	BOOL,
	STRING,
	CONST,

	TRUE,
	FALSE,

	INTEGER_NUMBER,
	REAL_NUMBER,
	BINARY_NUMBER,
	OCTAL_NUMBER,
	HEXADECIMAL_NUMBER,

	ADDITION,
	SUBTRACTION,
	MULTIPLICATION,
	DIVISION,

	LOGICAL_AND,
	LOGICAL_OR,
	LOGICAL_NOT,

	IF,
	ELSE,
	WHILE,
	FOR,
	BREAK,
	CONTINUE,
	READ,
	PRINT,

	CLASS,
	PUBLIC,
	PROTECTED,
	PRIVATE,

	OPENING_PARENTHESIS,
	CLOSING_PARENTHESIS,
	OPENING_BRACE,
	CLOSING_BRACE,

	EQUAL,
	NOT_EQUAL,

	LESS,
	GREATER,
	LESS_OR_EQUAL,
	GREATER_OR_EQUAL,

	ERROR,
};

struct Token
{
	TokenType type;
	std::string lexeme;
	std::size_t lineNumber;
	std::size_t columnNumber;
};
